// Print a to z.


public class Main 
{
    public static void main(String[] args) 
    {
        for(char ch = 'a'; ch <= 'z'; ch++)
        {
            System.out.println(ch);
        }
        
        
        System.out.println();
        System.out.println();
        
        
        for(int i = 97; i <= 122; i++)
        {
            System.out.println((char)i);
        }
    }
}
